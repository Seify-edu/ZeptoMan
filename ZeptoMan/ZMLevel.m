@interface ZMLevel()
{
    NSArray *_walls;
    NSMutableArray *_food;
}
@end

@implementation ZMLevel
- (NSArray *) walls
{
    if (!_walls)
    {
        NSMutableArray *tempArray = [NSMutableArray array];
        for (int i=0; i<16; i++){
            for (int j=0; j<12; j++) {
                if (level1_horizontal_walls[i + j*16] == 1){
                    
//                    NSLog(@"i = %d, j = %d, level1_horizontal_walls[%d] = %d", i, j, i + j*12,level1_horizontal_walls[i + j*16]);
                    
                    ZMSprite *wall = [[ZMSprite alloc] init];
                    wall.position = GLKVector2Make((float)i - 8.0 + 0.5, 6.0 - (float)j);
                    wall.width = 1.0;
                    wall.height = 0.2;
                    [wall setTextureImage:[UIImage imageNamed:@"wall.png"]];
                    [tempArray addObject:wall];
                }
            }
        }
        
        for (int i=0; i<17; i++){
            for (int j=0; j<12; j++) {
                if (level1_vertical_walls[i + j*17] == 1){
                    
                    
                    ZMSprite *wall = [[ZMSprite alloc] init];
                    wall.position = GLKVector2Make((float)i - 8.0, 6.0 - 0.5 - (float)j);
                    wall.width = 0.2;
                    wall.height = 1.0;
                    [wall setTextureImage:[UIImage imageNamed:@"wall.png"]];
                    [tempArray addObject:wall];
                }
            }
        }
        
    _walls = tempArray;
    }
    return _walls;
}
@end
